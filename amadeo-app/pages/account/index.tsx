import React from 'react';
import { getSession } from 'next-auth/client';
import { useTranslations } from 'next-intl';
import { getProfile } from '../../lib/profile';
import Head from 'next/head';
import { useState } from 'react';
import { Address, Profile, AddressesList } from '../../components/account';
import { getCountries } from '../../lib/staff';

interface ProfileProps {
    user: any;
    addresses: any;
    countries: any;
}

function Account({
    session,
    infoData,
    locale
}: {
    session: any;
    infoData: ProfileProps;
    locale: string;
}) {
    const t = useTranslations();

    const [activeTab, setActiveTab] = useState('profile');
    const [profileData] = useState(infoData.user);

    const handleTabClick = (e: React.MouseEvent<HTMLElement>) => {
        const target = e.target as HTMLElement;
        setActiveTab(target.id);
    };

    return (
        <div className="mr-8">
            <Head>
                <title>Amadeo CRM - Dashboard</title>
                <meta name="description" content="Generated by create next app" />
            </Head>

            <div className="page-title">
                <h1>
                    Profile <em /> <span>Personal Information</span>
                </h1>
            </div>
            <div className="flex-row w-full mr-10 bg-white rounded-lg p-4">
                <div className="block">
                    {profileData.role_id === 2 && (
                        <nav className="flex flex-col sm:flex-row">
                            <button
                                id="profile"
                                onClick={handleTabClick}
                                className={`tab-header ${activeTab === 'profile' ? 'active' : ''}`}>
                                {t('My profile')}
                            </button>
                            <button
                                id="addressess"
                                onClick={handleTabClick}
                                className={`tab-header ${
                                    activeTab === 'addressess' ? 'active' : ''
                                }`}>
                                {t('My Addressess')}
                            </button>
                        </nav>
                    )}
                </div>
                <div className="flex flex-col sm:flex-row">
                    <div className={`w-full ${activeTab !== 'profile' ? 'hidden' : ''}`}>
                        <Profile />
                    </div>
                    <div className={`w-full ${activeTab !== 'addressess' ? 'hidden' : ''}`}>
                        <AddressesList email={session.user.email || ''} />
                        <Address
                            userAddress={{
                                country_id: '',
                                state: '',
                                post_code: '',
                                address_type: '',
                                city: '',
                                address_line_1: '',
                                address_line_2: ''
                            }}
                            countriesData={infoData.countries}
                            email={session.user.email}
                            locale={locale}
                        />
                    </div>
                </div>
            </div>
        </div>
    );
}

export default Account;

export async function getServerSideProps(context: any) {
    const { req, locale } = context;
    const session = await getSession({ req });

    let infoData: ProfileProps;
    if (!session) {
        return {
            redirect: { destination: `/${locale === 'fr' ? '' : `${locale}/`}auth/signin` }
        };
    } else {
        infoData = await getProfile(session.user?.email);
        infoData.countries = await getCountries();
    }

    return {
        props: {
            session: session,
            locale: locale,
            infoData: infoData,
            messages: {
                ...require(`../../messages/${locale}.json`)
            }
        }
    };
}
